
public class SumOfEle_Array {

	public static void main(String[] args) {
		
		int arr[] = {2,3,5,6,7,9};
		int B=0;
		for(int i=0;i<arr.length;i++)
		{
			int A = arr[i];
			B=B+A;
		}
		System.out.println("The Sum of elements of an above array is: "+B);
		//The Sum of elements of an above array is: 32


	}

}
